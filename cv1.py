import cv2

# 이미지 불러오기
이미지 =  cv2.imread('img1.jpg')

#크기 변경
크기변경 = cv2.resize(이미지, (600,600))

# 글자넣기 cv2.putText(이미지, 넣을 글자, 위치, 폰트, 글자크기, 색깔, 글자굵기)
cv2.putText(크기변경, "Yejin Moon", (10,60), cv2.FONT_HERSHEY_PLAIN, 3, (255, 255, 255), 3)
# 빨간색: (0, 0, 255)
