# 노랑색: (0, 228, 255)
# 초록색: (0, 255, 0)
# 파란색: (255, 0, 0)
# 보라색: (255, 0, 95)
# 핑크색: (225, 0, 255)
# 흰색: (255, 255, 255)
# 검은색: (0, 0, 0)

# 이미지 보여주기 (제목, 이미지명)
cv2.imshow("cat", 크기변경)

